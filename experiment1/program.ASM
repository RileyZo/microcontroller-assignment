	ORG 	30H
	CYC1	EQU  200 
	CYC2	EQU  125 
	MOV 	A,#0FEH ;LED亮灯编码初值
	MOV 	P2,A
	MOV	R2,#7
DOWN:	RL	A	;下行方向
	ACALL 	DEL50
	MOV 	P2,A
	DJNZ 	R2,DOWN
	MOV 	R2,#7
UP:	RR 	A	;上行方向
	ACALL 	DEL50	
	MOV 	P2,A
	DJNZ 	R2,UP
	MOV	R2,#7
	SJMP 	DOWN

DEL50: 	MOV 	R7,#CYC1;延时50ms
DEL1:	MOV 	R6,#CYC2	
	DJNZ 	R6,$
	DJNZ 	R7,DEL1
	RET
	END
